def normalize_chinese(value: str) -> str:
    converter = _opencc_converter()
    if converter is not None:
        return converter.convert(value)
    return value.translate(_TRADITIONAL_TO_SIMPLIFIED)


@lru_cache(maxsize=1)
def _opencc_converter():
    try:
        from opencc import OpenCC  # type: ignore
    except Exception:
        return None
    try:
        return OpenCC("t2s")
    except Exception:
        return None
